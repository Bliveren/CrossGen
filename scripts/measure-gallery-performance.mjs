async function main() {
  const args = parseArgs(process.argv.slice(2));
  const manifestPath = path.join(args.fixture, "manifest.json");
  const manifest = await readJson(manifestPath);
  const state = await readJson(manifest.statePath);
  const stateStat = await fs.stat(manifest.statePath);

  if (!args.skipBuild) {
    run("pnpm", ["build:main"], { stdio: "inherit" });
  }

  const galleryDiskSync = await import(pathToFileURL(path.join(repoRoot, "dist", "main", "services", "galleryDiskSync.js")).href);
  const { scanGalleryDisk, reconcileGalleryDiskState, reconcileGalleryDiskChanges } = galleryDiskSync;

  await fs.mkdir(args.output, { recursive: true });

  const fullScan = await measureRepeated(
    "scanGalleryDisk full",
    args.iterations,
    () => scanGalleryDisk(manifest.galleryDir),
    summarizeDisk
  );
  const disk = await scanGalleryDisk(manifest.galleryDir);
  const sampleAssetRelPath = manifest.sampleAssetRelPaths?.[0] ?? state.galleryAssets[0]?.fileName;
  const sampleFolderRelPath = manifest.sampleFolderRelPath ?? state.galleryFolders[0]?.name;
  const incrementalFileScan = sampleAssetRelPath
    ? await measureRepeated(
      "scanGalleryDisk incremental file",
      args.iterations,
      () => scanGalleryDisk(manifest.galleryDir, { rootRelPaths: [sampleAssetRelPath] }),
      summarizeDisk
    )
    : null;
  const incrementalFolderScan = sampleFolderRelPath
    ? await measureRepeated(
      "scanGalleryDisk incremental folder",
      args.iterations,
      () => scanGalleryDisk(manifest.galleryDir, { rootRelPaths: [sampleFolderRelPath] }),
      summarizeDisk
    )
    : null;

  const reconcileFull = await measureRepeated(
    "reconcileGalleryDiskState full",
    args.iterations,
    () => reconcileGalleryDiskState(state, disk, createReconcileOptions()),
    summarizeState
  );
  const reconciledState = reconcileGalleryDiskState(state, disk, createReconcileOptions());
  const incrementalDisk = sampleAssetRelPath
    ? await scanGalleryDisk(manifest.galleryDir, { rootRelPaths: [sampleAssetRelPath] })
    : { folders: [], assets: [] };
  const reconcileIncremental = sampleAssetRelPath
    ? await measureRepeated(
      "reconcileGalleryDiskChanges incremental file",
      args.iterations,
      () => reconcileGalleryDiskChanges(state, incrementalDisk, [sampleAssetRelPath], createReconcileOptions()),
      summarizeState
    )
    : null;

  const handlerEquivalent = [];
  handlerEquivalent.push(await measureRepeated(
    "app:getSnapshot handler-equivalent",
    args.iterations,
    async () => {
      const nextDisk = await scanGalleryDisk(manifest.galleryDir);
      const nextState = reconcileGalleryDiskState(state, nextDisk, createReconcileOptions());
      const changed = galleryCollectionsChanged(state, nextState);
      return { ...summarizeState(nextState), stateWriteCountDelta: changed ? 1 : 0 };
    },
    (result) => result
  ));
  handlerEquivalent.push(await measureRepeated(
    "gallery:list handler-equivalent",
    args.iterations,
    async () => {
      const nextDisk = await scanGalleryDisk(manifest.galleryDir);
      const nextState = reconcileGalleryDiskState(state, nextDisk, createReconcileOptions());
      return { assetCount: nextState.galleryAssets.length, stateWriteCountDelta: galleryCollectionsChanged(state, nextState) ? 1 : 0 };
    },
    (result) => result
  ));
  handlerEquivalent.push(await measureRepeated(
    "galleryFolders:list handler-equivalent",
    args.iterations,
    async () => {
      const nextDisk = await scanGalleryDisk(manifest.galleryDir);
      const nextState = reconcileGalleryDiskState(state, nextDisk, createReconcileOptions());
      return { folderCount: nextState.galleryFolders.length, stateWriteCountDelta: galleryCollectionsChanged(state, nextState) ? 1 : 0 };
    },
    (result) => result
  ));

  const electronMain = args.noElectron ? { status: "skipped", reason: "--no-electron" } : await runElectronMainCapture(manifest, args.output);
  let viteServer = null;
  let electronRenderer = { status: "skipped", reason: "--no-renderer" };
  if (!args.noRenderer) {
    try {
      viteServer = await startViteServer();
      electronRenderer = await runElectronRendererCapture(manifest, args.output, viteServer.url);
      electronRenderer.devServer = {
        url: viteServer.url,
        ...viteServer.getOutput()
      };
    } finally {
      await viteServer?.stop();
    }
  }
  const commitSha = gitOutput(["rev-parse", "HEAD"]);
  const result = {
    capturedAt: new Date().toISOString(),
    commitSha,
    branch: gitOutput(["branch", "--show-current"]),
    fixture: {
      profile: manifest.profile,
      outputDir: manifest.outputDir,
      userDataDir: manifest.userDataDir,
      galleryDir: manifest.galleryDir,
      statePath: manifest.statePath,
      folderCount: manifest.folderCount,
      assetCount: manifest.assetCount,
      totalImageBytes: manifest.totalImageBytes,
      stateBytes: stateStat.size,
      maxDepth: manifest.maxDepth
    },
    machine: {
      platform: process.platform,
      osType: os.type(),
      osRelease: os.release(),
      arch: process.arch,
      cpuModel: os.cpus()[0]?.model ?? "unknown",
      cpuCount: os.cpus().length,
      totalMemoryBytes: os.totalmem()
    },
    commands: {
      fixture: manifest.commands?.regenerate,
      measure: `node scripts/measure-gallery-performance.mjs --fixture ${path.relative(repoRoot, args.fixture)} --output ${path.relative(repoRoot, args.output)}`,
      mainHandlerCapture: `CROSSGEN_USER_DATA_DIR=${manifest.userDataDir} CROSSGEN_PERF_RESULT_PATH=${path.join(args.output, "electron-main-handler-metrics.json")} electron .`,
      rendererCapture: `VITE_DEV_SERVER_URL=http://127.0.0.1:<port> CROSSGEN_USER_DATA_DIR=${manifest.userDataDir} CROSSGEN_RENDERER_PERF_RESULT_PATH=${path.join(args.output, "electron-renderer-metrics.json")} electron .`
    },
    metrics: {
      scans: [fullScan, incrementalFileScan, incrementalFolderScan].filter(Boolean),
      reconcile: [reconcileFull, reconcileIncremental].filter(Boolean),
      handlerEquivalent,
      electronMainHandlers: electronMain,
      stateFile: {
        path: manifest.statePath,
        bytes: stateStat.size,
        commonMutationWriteScenarios: commonMutationWriteScenarios(state, reconciledState)
      },
      thumbnails: thumbnailBaselineEstimate(reconciledState),
      renderer: {
        electronRenderer,
        timeToFirstGalleryGridRender: electronRenderer.status === "ok"
          ? {
            status: "ok",
            durationMs: electronRenderer.metrics.result.timings.timeToFirstGalleryGridRenderMs,
            galleryGrid: electronRenderer.metrics.result.galleryGrid
          }
          : {
            status: "capture-unavailable",
            reason: electronRenderer.reason ?? electronRenderer.stderr ?? "Renderer capture did not complete."
          },
        reactProfilerPartialImageTrace: electronRenderer.status === "ok"
          ? electronRenderer.metrics.result.reactProfilerPartialImageTrace
          : {
            status: "capture-unavailable",
            reason: electronRenderer.reason ?? electronRenderer.stderr ?? "Renderer capture did not complete."
          },
        accessibilitySmoke: electronRenderer.status === "ok"
          ? electronRenderer.metrics.result.accessibilitySmoke
          : {
            status: "capture-unavailable",
            reason: electronRenderer.reason ?? electronRenderer.stderr ?? "Renderer capture did not complete."
          },
        axeAccessibilitySmoke: electronRenderer.status === "ok"
          ? electronRenderer.metrics.result.axeAccessibilitySmoke
          : {
            status: "capture-unavailable",
            reason: electronRenderer.reason ?? electronRenderer.stderr ?? "Renderer capture did not complete."
          }
      }
    }
  };

  const resultPath = path.join(args.output, `${manifest.profile}-${commitSha.slice(0, 12)}.json`);
  await fs.writeFile(resultPath, `${JSON.stringify(result, null, 2)}\n`, "utf8");
  console.log(JSON.stringify({
    resultPath,
    fixtureProfile: manifest.profile,
    commitSha,
    fullScanMedianMs: fullScan.durationMs.median,
    appSnapshotMedianMs: handlerEquivalent[0].durationMs.median,
    electronMainStatus: electronMain.status,
    electronRendererStatus: electronRenderer.status
  }, null, 2));
}

main().catch((error) => {
  console.error(error instanceof Error ? error.stack ?? error.message : error);
  process.exit(1);
});
