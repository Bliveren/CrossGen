#!/usr/bin/env node

import { execFileSync, spawn } from "node:child_process";
import { promises as fs } from "node:fs";
import net from "node:net";
import os from "node:os";
import path from "node:path";
import { performance } from "node:perf_hooks";
import { fileURLToPath, pathToFileURL } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const defaultFixture = path.join(repoRoot, "output", "perf-fixtures", "gallery-large");
const defaultOutputDir = path.join(repoRoot, "output", "perf-baselines");
const fixedNow = "2026-01-02T00:00:00.000Z";

function usage() {
  return [
    "Usage: node scripts/measure-gallery-performance.mjs [--fixture output/perf-fixtures/gallery-large] [--output output/perf-baselines] [--skip-build]",
    "",
    "The fixture must be generated by scripts/create-large-gallery-fixture.mjs.",
    "The command never uses real API keys and should point CROSSGEN_USER_DATA_DIR at the fixture userData directory."
  ].join("\n");
}

function parseArgs(argv) {
  const args = {
    fixture: defaultFixture,
    output: defaultOutputDir,
    skipBuild: false,
    iterations: 3,
    noElectron: false,
    noRenderer: false
  };
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--help" || arg === "-h") {
      console.log(usage());
      process.exit(0);
    }
    if (arg === "--skip-build") {
      args.skipBuild = true;
      continue;
    }
    if (arg === "--no-electron") {
      args.noElectron = true;
      continue;
    }
    if (arg === "--no-renderer") {
      args.noRenderer = true;
      continue;
    }
    if (arg === "--fixture") {
      args.fixture = path.resolve(repoRoot, argv[index + 1] ?? "");
      index += 1;
      continue;
    }
    if (arg === "--output") {
      args.output = path.resolve(repoRoot, argv[index + 1] ?? "");
      index += 1;
      continue;
    }
    if (arg === "--iterations") {
      args.iterations = Math.max(1, Number.parseInt(argv[index + 1] ?? "3", 10));
      index += 1;
      continue;
    }
    throw new Error(`Unknown argument: ${arg}`);
  }
  return args;
}

function run(command, args, options = {}) {
  execFileSync(command, args, {
    cwd: repoRoot,
    stdio: options.stdio ?? "pipe",
    env: process.env
  });
}

function gitOutput(args) {
  return execFileSync("git", args, { cwd: repoRoot, encoding: "utf8" }).trim();
}

async function readJson(filePath) {
  return JSON.parse(await fs.readFile(filePath, "utf8"));
}

async function fileExists(filePath) {
  try {
    await fs.access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function portIsAvailable(port) {
  return await new Promise((resolve) => {
    const server = net.createServer();
    server.once("error", () => resolve(false));
    server.once("listening", () => {
      server.close(() => resolve(true));
    });
    server.listen(port, "127.0.0.1");
  });
}

async function waitForHttp(url, timeoutMs = 30000) {
  const startedAt = performance.now();
  let lastError;
  while (performance.now() - startedAt < timeoutMs) {
    try {
      const response = await fetch(url);
      if (response.ok) return;
    } catch (error) {
      lastError = error;
    }
    await new Promise((resolve) => setTimeout(resolve, 250));
  }
  throw new Error(`Timed out waiting for ${url}: ${lastError instanceof Error ? lastError.message : "no response"}`);
}

async function startViteServer() {
  let port = 5174;
  while (!(await portIsAvailable(port))) {
    port += 1;
    if (port > 5190) throw new Error("No free Vite performance port found in 5174-5190.");
  }
  const url = `http://127.0.0.1:${port}`;
  const child = spawn("pnpm", ["exec", "vite", "--host", "127.0.0.1", "--port", String(port), "--strictPort"], {
    cwd: repoRoot,
    env: process.env,
    detached: process.platform !== "win32",
    stdio: ["ignore", "pipe", "pipe"]
  });
  let stdout = "";
  let stderr = "";
  child.stdout.on("data", (chunk) => {
    stdout += chunk.toString();
  });
  child.stderr.on("data", (chunk) => {
    stderr += chunk.toString();
  });
  try {
    await waitForHttp(url);
  } catch (error) {
    child.kill("SIGTERM");
    throw Object.assign(error instanceof Error ? error : new Error(String(error)), {
      stdout,
      stderr
    });
  }
  return {
    url,
    stop: async () => {
      if (child.exitCode !== null) return;
      if (process.platform === "win32") {
        child.kill("SIGTERM");
      } else if (child.pid) {
        try {
          process.kill(-child.pid, "SIGTERM");
        } catch {
          child.kill("SIGTERM");
        }
      }
      await Promise.race([
        new Promise((resolve) => child.once("exit", resolve)),
        new Promise((resolve) => setTimeout(resolve, 5000))
      ]);
      if (child.exitCode === null) {
        if (process.platform === "win32") child.kill("SIGKILL");
        else if (child.pid) {
          try {
            process.kill(-child.pid, "SIGKILL");
          } catch {
            child.kill("SIGKILL");
          }
        }
      }
    },
    getOutput: () => ({ stdout: stdout.trim(), stderr: stderr.trim() })
  };
}

function median(values) {
  const sorted = [...values].sort((a, b) => a - b);
  return sorted[Math.floor(sorted.length / 2)] ?? 0;
}

async function measureRepeated(name, iterations, operation, summarize = () => ({})) {
  const durations = [];
  let lastResult;
  for (let index = 0; index < iterations; index += 1) {
    const startedAt = performance.now();
    lastResult = await operation();
    durations.push(performance.now() - startedAt);
  }
  return {
    name,
    iterations,
    durationMs: {
      min: Math.min(...durations),
      median: median(durations),
      max: Math.max(...durations),
      all: durations
    },
    result: summarize(lastResult)
  };
}

function createReconcileOptions() {
  let folderIndex = 0;
  let assetIndex = 0;
  return {
    now: fixedNow,
    createFolderId: () => `measure_folder_${String(folderIndex += 1).padStart(5, "0")}`,
    createAssetId: () => `measure_asset_${String(assetIndex += 1).padStart(5, "0")}`
  };
}

function galleryCollectionsChanged(before, after) {
  return (
    JSON.stringify(before.galleryFolders) !== JSON.stringify(after.galleryFolders) ||
    JSON.stringify(before.galleryAssets) !== JSON.stringify(after.galleryAssets)
  );
}

function statePayloadBytes(state) {
  return Buffer.byteLength(`${JSON.stringify(state, null, 2)}\n`, "utf8");
}

function summarizeDisk(disk) {
  return {
    folderCount: disk.folders.length,
    assetCount: disk.assets.length,
    totalAssetBytes: disk.assets.reduce((sum, asset) => sum + asset.sizeBytes, 0)
  };
}

function summarizeState(state) {
  return {
    folderCount: state.galleryFolders.length,
    assetCount: state.galleryAssets.length,
    statePayloadBytes: statePayloadBytes(state)
  };
}

function commonMutationWriteScenarios(state, reconciledState) {
  const scenarios = [];
  scenarios.push({
    name: "full sync with no Gallery changes",
    stateWriteCount: galleryCollectionsChanged(state, reconciledState) ? 1 : 0,
    statePayloadBytes: statePayloadBytes(reconciledState)
  });

  const firstAsset = reconciledState.galleryAssets[0];
  if (firstAsset) {
    const nextState = {
      ...reconciledState,
      galleryAssets: reconciledState.galleryAssets.map((asset) => (
        asset.id === firstAsset.id ? { ...asset, tags: [...new Set([...asset.tags, "measure-tag"])] } : asset
      ))
    };
    scenarios.push({
      name: "Gallery tag update",
      stateWriteCount: 1,
      statePayloadBytes: statePayloadBytes(nextState)
    });
  }

  const firstFolder = reconciledState.galleryFolders[0];
  if (firstFolder) {
    const nextState = {
      ...reconciledState,
      galleryFolders: reconciledState.galleryFolders.map((folder) => (
        folder.id === firstFolder.id ? { ...folder, name: `${folder.name}-renamed`, updatedAt: fixedNow } : folder
      ))
    };
    scenarios.push({
      name: "Gallery folder rename",
      stateWriteCount: 1,
      statePayloadBytes: statePayloadBytes(nextState)
    });
  }

  const movableAsset = reconciledState.galleryAssets.find((asset) => asset.folderId === null) ?? firstAsset;
  const targetFolder = reconciledState.galleryFolders[0];
  if (movableAsset && targetFolder) {
    const nextState = {
      ...reconciledState,
      galleryAssets: reconciledState.galleryAssets.map((asset) => (
        asset.id === movableAsset.id ? { ...asset, folderId: targetFolder.id, updatedAt: fixedNow } : asset
      ))
    };
    scenarios.push({
      name: "Gallery asset move",
      stateWriteCount: 1,
      statePayloadBytes: statePayloadBytes(nextState)
    });
  }

  return scenarios;
}

function thumbnailBaselineEstimate(state, sampleSize = 40) {
  const sampledAssets = state.galleryAssets.slice(0, sampleSize);
  return {
    currentBehavior: "Gallery card images request full original asset URLs; Phase 3 should replace these with thumbnails.",
    sampledCardCount: sampledAssets.length,
    requestCount: sampledAssets.length,
    totalBytesServed: sampledAssets.reduce((sum, asset) => sum + asset.sizeBytes, 0),
    averageBytesPerRequest: sampledAssets.length === 0 ? 0 : sampledAssets.reduce((sum, asset) => sum + asset.sizeBytes, 0) / sampledAssets.length
  };
}

async function runElectronMainCapture(manifest, outputDir) {
  const electronBin = path.join(repoRoot, "node_modules", ".bin", process.platform === "win32" ? "electron.cmd" : "electron");
  if (!(await fileExists(electronBin))) {
    return {
      status: "skipped",
      reason: `Electron binary not found at ${electronBin}. Run pnpm install first.`
    };
  }

  await fs.mkdir(outputDir, { recursive: true });
  const resultPath = path.join(outputDir, "electron-main-handler-metrics.json");
  await fs.rm(resultPath, { force: true });
  const child = spawn(electronBin, [repoRoot], {
    cwd: repoRoot,
    env: {
      ...process.env,
      CROSSGEN_USER_DATA_DIR: manifest.userDataDir,
      CROSSGEN_PERF_RESULT_PATH: resultPath,
      ELECTRON_DISABLE_SECURITY_WARNINGS: "true"
    },
    stdio: ["ignore", "pipe", "pipe"]
  });

  let stdout = "";
  let stderr = "";
  child.stdout.on("data", (chunk) => {
    stdout += chunk.toString();
  });
  child.stderr.on("data", (chunk) => {
    stderr += chunk.toString();
  });

  const exitCode = await new Promise((resolve) => {
    const timeout = setTimeout(() => {
      child.kill("SIGTERM");
      resolve("timeout");
    }, 60000);
    child.on("exit", (code) => {
      clearTimeout(timeout);
      resolve(code);
    });
  });

  if (exitCode !== 0) {
    return {
      status: "failed",
      exitCode,
      stdout: stdout.trim(),
      stderr: stderr.trim()
    };
  }

  return {
    status: "ok",
    resultPath,
    metrics: await readJson(resultPath),
    stdout: stdout.trim(),
    stderr: stderr.trim()
  };
}

